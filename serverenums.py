"""
CLientResponse is the enum for all return messages to the client
RoomRequest is the enum for all messages to the room server
GameRequest is the enum for all messages to the game server
RoomStatus is the enum for managing the room
"""

from enum import Enum

class MyEnum(Enum):

	def __str__(self):
		return "({0}={1})".format(self.__class__.__name__, self.name)

